/* ─── Utility ─── */
function i18nGet(key) {
  const lang = (localStorage.getItem('bozcorp_lang') || 'tr');
  const dict = BOZCORP_I18N[lang] || BOZCORP_I18N['tr'];
  return dict[key] || BOZCORP_I18N['tr'][key] || key; 
}

function i18nApply() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = i18nGet(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  // Yeni: Title + Desc kombinasyonu
  document.querySelectorAll('.timeline-card').forEach(card => {
    const titleKey = card.getAttribute('data-i18n-title');
    const descKey  = card.getAttribute('data-i18n-desc');

    if (titleKey) {
      const h4 = card.querySelector('h4');
      if (h4) h4.textContent = i18nGet(titleKey);
    }

    if (descKey) {
      const p = card.querySelector('p');
      if (p) p.textContent = i18nGet(descKey);
    }
  });
}

